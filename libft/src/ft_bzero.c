#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	c;
	char	*s_tmp;

	s_tmp = (char *) s;
	if (n < 1 )
		return ;
	c = 0;
	while (s_tmp[c] != '\0' && c < n)
	{
		s_tmp[c] = '\0';
		c++;
	}
}
