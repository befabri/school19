#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	char			*dst;
	unsigned int	total;
	unsigned int	i;

	total = count * size;
	if (!(dst = malloc(total)))
		return (0);
	i = 0;
	while (total--)
	{
		dst[i] = 0;
		i++;
	}
}
