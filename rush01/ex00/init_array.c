void	init_array(int tablo[6][6])
{
	int	r;
	int	c;

	r = 0;
	while (r < 6)
	{
		c = 0;
		while (c < 6)
		{
			tablo[r][c] = 0;
			c++;
		}
		r++;
	}
}
